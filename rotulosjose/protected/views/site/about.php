
<h1>Quienes somos</h1>
<p>En Rótulos José, contamos con una experiencia de más de 25 años y con cualificados trabajadores en plantilla de alta especialización y formación, disponemos de una moderna maquinaria y medios de producción para realizar los encargos y trabajos que nos son confiados: herramientas y máquinas para el montaje, herramientas y máquinas para fabricación, plóter de corte, plóter de impresión digital, laminadora y diversos equipos informáticos. </p>
<p>Se nos distingue claramente de la competencia del sector de la rotulación, la cartelería y la publicidad exterior, nuestro precio, calidad y servicio, así como unos plazos de entrega muy ajustados y unos diseños frescos e innovadores como así avalan nuestros clientes con su fidelidad y satisfacción.</p>

<p>Damos servicio a nivel nacional y estamos especializados en muy diversos trabajos:</p>

<ul>
<li>Rotulación de vehículos: automóviles, furgonetas, autocares, camiones, etc.</li>
<li>Rotulación de fachadas.</li>
<li>Rótulos luminosos.</li>
<li>Lonas plásticas.</li>
<li>Rótulos corpóreos.</li>
<li>Vallas publicitarias.</li>
<li>Cartelería</li>
<li>Impresión digital de pequeño y gran formato.</li>
</ul>



